def dodawanie(a, b):
    """

    Args:
        a: składnik a
        b: składnik b

    Returns: zwraca wynik dodawania

    """
    return a + b


def odejmowanie(a, b):
    """

    Args:
        a: parametr 1
        b: parametr 2

    Returns: zwraca wynik odejmowania

    """
    return a - b


def mnozenie(a, b):
    """

    Args:
        a: parametr 1
        b: parametr 2

    Returns: zwraca wynik mnożenia

    """
    return a * b


def dzielenie(a, b):
    """

    Args:
        a: parametr 1
        b: parametr 2

    Returns: zwraca wynik dzielenia

    """
    return a / b
